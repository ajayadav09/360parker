// ==================================================== Clicking on the parts and traverse to the part =================================== //
	
$("#part-1").on("click", function( e ) {
            e.preventDefault();
			$('#popupOne').hide();
			$('.overlay').hide();
			$('#productnum p:nth-child(2)').css('display', 'block');
			$('#productnum  p:not(:nth-child(2))').css('display', 'none');
			$('.blackstrip img:nth-child(2)').css('display', 'block');
			$('.blackstrip img:not(:nth-child(2))').css('display', 'none');
			car.jump1();
			compcounter(1);
			imgcounter(1);
			popinfocounter(1);
			
});


